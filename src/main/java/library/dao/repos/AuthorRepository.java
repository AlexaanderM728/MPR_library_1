package library.dao.repos;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

import library.domain.Author;
import library.domain.Person;

public class AuthorRepository {
	
	String url = "jdbc:hsqldb:hsql://localhost/workdb";
	
	Connection connection;
	
	private boolean tableExists;
	
	PreparedStatement insert;
	PreparedStatement selectById;
	PreparedStatement count;
	PreparedStatement lastId;
	PreparedStatement selectPage;
	
	public AuthorRepository(){
		
		try {
			
			connection = DriverManager.getConnection(url);
			
			insert = connection.prepareStatement(""
					+ "INSERT INTO author(first_name, last_name) VALUES (?,?)"
					+ "");
			
			ResultSet rs = connection.getMetaData().getTables(null, null, null, null);
			
			while(rs.next()){
				if(rs.getString("TABLE_NAME").equalsIgnoreCase("author"))
					tableExists=true;
			}
		
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}
	
	public Author get(int id){
		
		Author result = null;
		try {
			selectById.setInt(1, id);
			ResultSet rs = selectById.executeQuery();
			
			while(rs.next()){
				result = new Author();
				result.setId(rs.getInt("id"));
				result.setFirst_name(rs.getString("first_name"));
				result.setLast_name(rs.getString("last_name"));
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return result;
	}
	
	public PreparedStatement count(int id){
		
		PreparedStatement result = null;
		try {
			connection = DriverManager.getConnection(url);
			
			count = connection.prepareStatement("select count(id) from author");
			
			result = count;
			
			ResultSet rs = connection.getMetaData().getTables(null, null, null, null);
			
			while(rs.next()){
				if(rs.getString("TABLE_NAME").equalsIgnoreCase("author"))
					tableExists=true;
			}
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
		return result;
	}
	
	
	
	public void add(Author author){
		
		try {
			insert.setString(1, author.getFirst_name());
			insert.setString(2, author.getLast_name());
			insert.executeUpdate();
		} catch (SQLException e) {
			e.printStackTrace();
		} 
		
		
	}
	
	public void createTable(){
		
		String sql = "CREATE TABLE author("
				+ "id bigint GENERATED BY DEFAULT AS IDENTITY,"
				+ "first_name varchar(20),"
				+ "last_name varchar(50)"
				+ ")";
		
		try {
			Statement createTable = connection.createStatement();
			if(!tableExists)
				createTable.executeUpdate(sql);
			
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}

}
